# creating a list
List = ['p','y','t','h','o','n','f','o','r','g','a','m','e','s']
print('initial List : ')
print(List)

sliced_List = List[3:8]
print("\n slicing elements in a range 3-8: ", sliced_List)
sliced_List = List[:-6]
print("\n slicing elements in a range 0-(-6): ", sliced_List)

sliced_List = List[5:]
print("\n slicing elements in a range 5-rest: ", sliced_List)
